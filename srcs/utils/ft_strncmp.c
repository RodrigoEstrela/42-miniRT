#include "../../inc/minirt.h"

int	ft_strncmp(char *s1, char *s2, int n)
{
	int	i;

	i = 0;
	if (n == 0)
		return (0);
	if (!s1 || !s2)
		return (0);
	while (i + 1 < n && ((unsigned char)s1[i] == (unsigned char)s2[i]
			&& ((unsigned char)s1[i]) && ((unsigned char)s2[i])))
		i++;
	return ((unsigned char)s1[i] - (unsigned char)s2[i]);
}
